using namespace std;

struct trie {
    trie *data[10];
    trie() : data{nullptr} {}
};

class Solution {
  public:
    int longestCommonPrefix(vector<int> &arr1, vector<int> &arr2) {
        trie *root = createTrie(arr1); // initialize trie with arr1

        // solution
        int longest{};                   // answer to return
        for (int &e : arr2) {            // for per int element in arr2
            string tmpStr{to_string(e)}; // convert to string
            trie *cur{root};             // create a cur pointer to trie root
            int tmpMax{}; // store longest common prefix for current string

            for (char &c : tmpStr) { // for per character in string
                if (cur->data[c - '0'] != nullptr) {
                    ++tmpMax;
                    cur = cur->data[c - '0'];
                } else
                    break; // stop iteration of current string when not match
            }
            longest = (tmpMax > longest) ? tmpMax : longest;
        }

        size_t deleteCount{deleteTrie(root)}; // clearing up dynamic memory
        // clog << "deleted: " << deleteCount << '\n';
        return longest;
    }

  private:
    trie *createTrie(vector<int> &arr) {
        trie *root{new trie()};
        for (int &e : arr) {             // for per arr int element
            string tmpStr{to_string(e)}; // convert to string
            trie *cur{root};
            for (char &c : tmpStr) { // for per character of string
                if (cur->data[c - '0'] == nullptr)
                    cur->data[c - '0'] = {new trie()};
                cur = cur->data[c - '0'];
            }
        }
        return root;
    }

    size_t deleteTrie(trie *root) {
        size_t deleted = deleteTrieSub(root);
        delete root;
        root = nullptr;
        return deleted;
    }

    size_t deleteTrieSub(trie *cur) {
        size_t deleted{};
        for (trie *dataElement : cur->data) {
            if (dataElement == nullptr)
                continue;
            deleted += deleteTrieSub(dataElement);
            delete dataElement;
            dataElement = nullptr;
            ++deleted;
        }
        return deleted;
    }
};

