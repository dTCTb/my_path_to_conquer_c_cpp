class Solution {
  public:
    // other's solution
    int minSubarray(vector<int> &nums, int p) {
        int n{static_cast<int>(nums.size())};
        const int totalSum{
            accumulate(nums.cbegin(), nums.cend(), 0,
                       [&](int acc, int v) { return (acc + v) % p; })};
        const int target{totalSum % p};

        if (target == 0)
            return 0;

        unordered_map<int, int> residual;
        residual[0] = -1;
        int currentSum{}, minLen{n};

        for (int i{}; i < n; ++i) {
            currentSum = (currentSum + nums[i]) % p;
            int needed{(currentSum - target + p) % p};

            if (residual.find(needed) != residual.end())
                minLen = min(minLen, i - residual[needed]);

            residual[currentSum] = i;
        }

        return minLen == n ? -1 : minLen;
    }

    // testcase 137/142 passed
    int my_minSubarray(vector<int> &nums, int p) {
        unordered_set<int> residual;
        // unordered_map<int, int> residual;
        function<long(long, int)> prefixSumHelper = [&](long acc,
                                                        int v) -> long {
            residual.insert(v % p);
            // acc += v;
            // residual[(v % p + p) % p] = acc; // if using unordered_map
            // instead residual[acc] = v % p;
            return acc += v;
        };
        const long acc =
            accumulate(nums.cbegin(), nums.cend(), 0L, prefixSumHelper);
        const long target = acc % p;

        if (target == 0)
            return 0;
        if ((residual.find(target) != residual.end()))
            return 1;

        set<int> ll;
        for (int i{}; i < nums.size(); ++i) {
            long tmpRemainder{}, subArray{};
            for (int j{i}; j < nums.size(); ++j) {
                int needed{(nums[j] % p + p) % p};
                if (needed == 0)
                    break;
                tmpRemainder += needed;
                ++subArray;
                if (tmpRemainder % p == target) // &&
                    ll.insert(subArray);
                if (tmpRemainder > target * 2)
                    break;
            }
        }

        // print(ll);
        // print(residual);
        // print(acc, target);

        return ll.empty() || *(ll.begin()) == nums.size() ? -1 : *(ll.begin());
    }

  private:
    void print(int acc, int target) {
        cout << "prefix sum: " << acc << "\ntarget: " << target << '\n';
    }
    void print(unordered_map<int, int> &map) {
        for (unordered_map<int, int>::value_type n : map)
            cout << n.first << " : " << n.second << '\n';
    }
    void print(map<int, int> &map) {
        for (unordered_map<int, int>::value_type n : map)
            cout << n.first << " : " << n.second << '\n';
    }
    void print(unordered_set<int> &set) {
        for (unordered_set<int>::value_type n : set)
            cout << n << ' ';
        cout.put('\n');
    }
    void print(set<int> &set) {
        for (unordered_set<int>::value_type n : set)
            cout << n << ' ';
        cout.put('\n');
    }
};

