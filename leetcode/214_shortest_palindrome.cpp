class Solution {
  public:
    string shortestPalindrome(string s) {
        string ret;
        for (auto i = s.crbegin(); i != s.crend(); ++i) {
            string tmp{s.crbegin(), i};
            bool isEnd{false};
            // for (size_t j{}; j < s.size() && s[j] == *(i + j); j++) {
            for (size_t j{}; j < s.size() - (i - s.crbegin()); j++) {
                if (s[j] != *(i + j))
                    break;
                tmp += s[j];
                if ((i + j + 1) == s.crend()) {
                    ret = tmp + s.substr(j + 1);
                    isEnd = true;
                }
            }
            if (isEnd)
                break;
        }
        return ret;
    }
};

